    public void goToOrder(int space) {
        SpaceController controller = new SpaceController();
        boolean response = controller.selectSpace(spacesDataList.get(space));
        if (response) {
            OrdersFrame orderFrame = new OrdersFrame();
            dispose();
            orderFrame.setLocationRelativeTo(null);
            orderFrame.setVisible(true);
            FoodSelected food = FoodSelected.getInstance();
            food.addObserver(orderFrame);
        }
    }
    
